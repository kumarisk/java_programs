import java.util.*;
class Input_Ex
{
    public static void main(String args[])
    {
         Scanner sc=new Scanner(System.in);
         System.out.println("\nEnter No1:");
         int no1=sc.nextInt();
         System.out.println("\nEnter no2:");
         float no2=sc.nextFloat();
         System.out.println("\nEnter Text:");
         String txt=sc.nextLine();
        System.out.println("\nNo1:"+no1);
        System.out.println("\nNo2:"+no2);
        System.out.println("\nText:"+txt);
   }
}
