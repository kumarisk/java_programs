/*using Enhanced for loop print even numbers from array*/
class EvenEx
{
     public static void main(String args[])
    {
         int a[]={4,61,8,10,5};
         System.out.println("\nEven Numbers:");
         for(int c:a)
          {
             if(c%2==0)
                System.out.print(" "+c);
          }
    }
}
         
 